class RandomProxy(object):
    def __init__(self):
        super().__init__()
        self.proxy_list = self.from_ip_pool_get_ip()

    def process_request(self, request, spider):
        # 随机代理ip
        ip = random.choice(self.proxy_list)

        # result = self.judge_ip_is_effective(ip)
        if ip:
            print('==================| 现在使用的 代理ip:%s' % ip)
            request.meta['proxy'] = ip
            request.meta['download_timeout'] = 60

    def from_ip_pool_get_ip(self):
        base_url = 'http://127.0.0.1:8000'
        result = requests.get(base_url).json()

        result_ip_list = []
        for item in result:
            tmp_url = 'http://' + str(item[0]) + ':' + str(item[1])
            result_ip_list.append(tmp_url)
        # print(result_ip_list)
        return result_ip_list
